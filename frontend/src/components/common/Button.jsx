import React from 'react';
import { motion } from 'framer-motion';

const Button = ({
  children,
  variant = 'primary',
  size = 'md',
  disabled = false,
  loading = false,
  icon,
  iconPosition = 'left',
  className = '',
  onClick,
  type = 'button',
  fullWidth = false,
  ...props
}) => {
  const baseClasses = `
    inline-flex items-center justify-center font-medium rounded-xl
    transition-all duration-300 focus:outline-none focus:ring-2 focus:ring-offset-2
    disabled:opacity-50 disabled:cursor-not-allowed disabled:pointer-events-none
    ${fullWidth ? 'w-full' : ''}
  `;

  const variants = {
    primary: `
      bg-gradient-to-r from-primary to-primary/80 text-white
      hover:from-primary/90 hover:to-primary/70
      focus:ring-primary/50 shadow-lg shadow-primary/25
      hover:shadow-xl hover:shadow-primary/30
    `,
    secondary: `
      bg-surface/50 backdrop-blur-sm text-white border border-white/10
      hover:bg-surface/70 hover:border-white/20
      focus:ring-white/20 shadow-lg shadow-black/10
      hover:shadow-xl hover:shadow-black/20
    `,
    outline: `
      bg-transparent text-white border-2 border-primary
      hover:bg-primary/10 hover:border-primary/80
      focus:ring-primary/50
    `,
    ghost: `
      bg-transparent text-gray-300 hover:text-white
      hover:bg-white/5 focus:ring-white/20
    `,
    danger: `
      bg-gradient-to-r from-red-500 to-red-600 text-white
      hover:from-red-600 hover:to-red-700
      focus:ring-red-500/50 shadow-lg shadow-red-500/25
      hover:shadow-xl hover:shadow-red-500/30
    `,
    success: `
      bg-gradient-to-r from-green-500 to-green-600 text-white
      hover:from-green-600 hover:to-green-700
      focus:ring-green-500/50 shadow-lg shadow-green-500/25
      hover:shadow-xl hover:shadow-green-500/30
    `
  };

  const sizes = {
    sm: 'px-3 py-2 text-sm',
    md: 'px-4 py-3 text-base',
    lg: 'px-6 py-4 text-lg',
    xl: 'px-8 py-5 text-xl'
  };

  const buttonClasses = `
    ${baseClasses}
    ${variants[variant]}
    ${sizes[size]}
    ${className}
  `;

  const LoadingSpinner = () => (
    <motion.div
      className="w-4 h-4 border-2 border-current border-t-transparent rounded-full"
      animate={{ rotate: 360 }}
      transition={{ duration: 1, repeat: Infinity, ease: "linear" }}
    />
  );

  const IconWrapper = ({ children, position }) => (
    <motion.div
      className={`flex items-center ${position === 'right' ? 'ml-2' : 'mr-2'}`}
      initial={{ scale: 0.8, opacity: 0 }}
      animate={{ scale: 1, opacity: 1 }}
      transition={{ duration: 0.2 }}
    >
      {children}
    </motion.div>
  );

  return (
    <motion.button
      type={type}
      className={buttonClasses}
      onClick={onClick}
      disabled={disabled || loading}
      whileHover={{ scale: disabled || loading ? 1 : 1.02 }}
      whileTap={{ scale: disabled || loading ? 1 : 0.98 }}
      initial={{ opacity: 0, y: 10 }}
      animate={{ opacity: 1, y: 0 }}
      transition={{ duration: 0.3 }}
      {...props}
    >
      {loading ? (
        <>
          <LoadingSpinner />
          <span className="ml-2">Loading...</span>
        </>
      ) : (
        <>
          {icon && iconPosition === 'left' && (
            <IconWrapper position="left">
              {icon}
            </IconWrapper>
          )}
          <motion.span
            initial={{ opacity: 0 }}
            animate={{ opacity: 1 }}
            transition={{ delay: 0.1 }}
          >
            {children}
          </motion.span>
          {icon && iconPosition === 'right' && (
            <IconWrapper position="right">
              {icon}
            </IconWrapper>
          )}
        </>
      )}
    </motion.button>
  );
};

